public class ControlFlow {
    public static void main(String[] args){
        int visaCard = 100;
        int burgerCost = 45;

        if(visaCard >= burgerCost){
            System.out.println("Congrats on your fancy burger.");
        }

        int lowCard = 40;

        if(lowCard >= burgerCost){
            System.out.println("Congrats on your fancy burger.");
        }
    }
}
